          <p>the Webb mission is intended to last for 5.5 to 10 years.</p>
          <p>he project is a collaboration between NASA, the European Space Agency (ESA), and the Canadian Space Agency.</p>
          <p>the telescope is designed to observe stars and galaxies that formed over 13.5 billion years ago.</p>
          <p>one of the main goals for the project is to study scientific mysteries revolving around the origins of the universe and where humans belong in it.</p>
          <p>Webb can observe exoplanetary atmospheres (planets that orbit other stars) and learn about their chemical compositions through a technique known as transmission spectroscopy</p>
          <p>the 18 mirrors of the Webb telescope are beryllium and gold-plated hexagonal shapes</p>
          <p>it is the largest and most powerful space science telescope created by NASA</p>
          <p>the building of the James Webb Space Telescope involved thousands of scientists and engineers from 29 U.S. states, 14 countries, and over 300 universities.</p>
          <p>the Webb telescope is 100 times more powerful than the Hubble.</p>
          <p>to protect the telescope from infrared radiation of the Sun, Earth, and Moon, the telescope has a 5-layer sunshield that equivalent to using SPF 1 million</p>
